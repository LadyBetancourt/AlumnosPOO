namespace AlumnosPOO
{
    internal class Alumno
    {
        string nombre;
        string grupo;
        string contrasena;

        public string Nombre
        {
            get
            {
                return nombre;
            }
            set
            {
                nombre = value;
            }
        }

        public string Grupo
        {
            get
            {
                return grupo;
            }
            set
            {
                grupo = value;
            }
        }
        public string Contrasena
        {
            get
            {
                return contrasena;
            }
            set
            {
                contrasena = value;
            }
        }
    }
}
